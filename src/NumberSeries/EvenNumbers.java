package NumberSeries;

public class EvenNumbers {
	
	public static void main(String[] args) {
		
		// print even Numbers from 1 to N
		
		for(int i=0;i<=20;i++)
		{
			if(i%2==0)
			{
				System.out.println("Even numbers = "+i);
				System.out.println();
			}
		
			else 
			{
				System.out.println("odd even number = "+i);
			}
		}
	}

}
